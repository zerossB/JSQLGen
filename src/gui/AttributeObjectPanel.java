    /** Creates new form AttributeObjectPanel */
    public AttributeObjectPanel(project.Project projectBean, project.ClassBean classBean) {
        this.projectBean = projectBean;
        initComponents();

        //atualiza lista de classes
        for(project.ClassBean classBeanItem: projectBean.getClassBeans()){
            if(classBeanItem != classBean && !classBeanItem.getUnique().isEmpty())
                cClassRef.addItem(classBeanItem.getName());
        }
    }

    @Override
    public void setAtribute(project.Attribute attribute){
        project.AttributeObject attObject = (project.AttributeObject)attribute;
        cClassRef.setSelectedItem(attObject.getClassRef());
        cAttribLookUp.setSelectedItem(attObject.getAttribLookUp());
    }

    @Override
    public project.AttributeObject getAtribute(){
        project.AttributeObject att = new project.AttributeObject();
        att.setClassRef(cClassRef.getSelectedItem().toString());
        att.setAttribLookUp(cAttribLookUp.getSelectedItem().toString());
        return att;
    }

    @Override
    public String validateForm(){
        String error = "";
        if(cClassRef.getSelectedItem()==null) error+= "Classe de referência\n";
        if(cAttribLookUp.getSelectedItem()==null) error+= "Atributo de visualização\n";
        return error;
    }

