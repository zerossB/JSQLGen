    private void cClassRefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cClassRefActionPerformed
        if(cClassRef.getSelectedItem()!=null){
            cAttribLookUp.setSelectedItem(null);
            cAttribLookUp.removeAllItems();
            java.util.List<project.Attribute> attributes = projectBean.getClassBean(cClassRef.getSelectedItem().toString()).getAttributes();
            for(project.Attribute att: attributes){
                if(att.isUnique())
                    cAttribLookUp.addItem(att.getName());
            }
        }
    }//GEN-LAST:event_cClassRefActionPerformed

