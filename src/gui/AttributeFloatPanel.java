    @Override
    public void setAtribute(project.Attribute attribute){
        project.AttributeFloat attFloat = (project.AttributeFloat)attribute;
        cIndexed.setSelected(attFloat.isIndexed());
        tStartValue.setText(attFloat.getStartValueF());
        tMaskFormat.setText(attFloat.getMaskFormat());
    }

    @Override
    public project.AttributeFloat getAtribute(){
        project.AttributeFloat att = new project.AttributeFloat();
        att.setIndexed(cIndexed.isSelected());
        att.setStartValueF(tStartValue.getText());
        att.setMaskformat(tMaskFormat.getText());
        return att;
    }

    @Override
    public String validateForm(){
        String error = "";
        if(tStartValue.getText().length()==0) error+= "Valor Inicial\n";
        return error;
    }

